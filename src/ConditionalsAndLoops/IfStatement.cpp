#include <iostream>
using namespace std;

// the if statement is good when we want to execute a block of code,
// when a certain condition is satisfied.

int main()
{
    int a = 55;
    int b = 33;
    if (a > b) {
        // this runs the cout part only if a is bigger than b.
        cout << "a is greater than b";
    }

    return 0;
}
